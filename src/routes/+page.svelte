	měsíční/týdenní přehledů akcí, generátor textací pro FB události (pro zachování struktury u každé
	akce), automatický export do Guidle (služba 3. strany spravující (mimo jiné) oficiální městský
	kalendář akcí) a různé analytické a statistické nástroje (kolik nových kapel u nás hrálo v roce x,
	jaký žánr byl tenhle měsíc nejhranější, apod.). Časem je plánovaný update s automatizací celého
	backofficu, který provoz klubu potřebuje.<br />
